#include <unistd.h>

void	ft_is_negative(int n)
{
	int	x;
	int	y;

	x = 80;
	y = 78;
	if (n >= 0)
	{
		write (1, &x, 1);
	}
	else
	{
		write (1, &y, 1);
	}
}
